/**
 * Table Create Mutation
 * Handles table creation with schema support
 */

import { tableKeys } from './keys.js';

/**
 * Create table payload type
 * @typedef {Object} CreateTableBody
 * @property {string} name - Table name
 * @property {string} [schema] - Schema name
 * @property {string|null} [comment] - Table comment/description
 */

/**
 * Create table variables type
 * @typedef {Object} TableCreateVariables
 * @property {string} projectRef - Project reference
 * @property {string|null} [connectionString] - Database connection string (optional)
 * @property {CreateTableBody & {schema: string}} payload - Table creation payload
 */

/**
 * Create a new database table
 * @async
 * @param {TableCreateVariables} params - Creation parameters
 * @returns {Promise<void>} Result of table creation
 * @throws {Error} If table creation fails
 */
async function createTable({ projectRef, connectionString, payload }) {
  try {
    // Build SQL for table creation using pgMeta pattern
    const tableSchema = payload.schema || 'public';
    const tableName = payload.name;
    const comment = payload.comment ? `'${payload.comment.replace(/'/g, "''")}'` : null;

    let sql = 'CREATE TABLE ' + tableSchema + '.' + tableName + ' (\n';
    sql += '  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,\n';
    sql += '  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE(\'utc\'::text, NOW()),\n';
    sql += '  updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE(\'utc\'::text, NOW())\n';
    sql += ');\n';

    if (comment) {
      sql += 'COMMENT ON TABLE ' + tableSchema + '.' + tableName + ' IS ' + comment + ';\n';
    }

    // Execute SQL query via main process
    const { ipcRenderer } = require('electron');
    const result = await ipcRenderer.invoke('execute-sql', {
      projectRef,
      connectionString,
      sql,
      queryKey: ['table', 'create'],
    });

    return result;
  } catch (error) {
    console.error('Error creating table:', error);
    throw error;
  }
}

/**
 * React Query mutation hook for table creation
 * @param {Object} options - Mutation options
 * @param {Function} [options.onSuccess] - Success callback
 * @param {Function} [options.onError] - Error callback
 * @returns {Object} Mutation object with mutate function
 */
function useTableCreateMutation({ onSuccess, onError } = {}) {
  return {
    /**
     * Execute table creation mutation
     * @async
     * @param {TableCreateVariables} variables - Creation variables
     */
    mutateAsync: async (variables) => {
      try {
        const data = await createTable(variables);
        const { projectRef, payload } = variables;

        // Invalidate related queries
        const { ipcRenderer } = require('electron');
        await ipcRenderer.invoke('cache-invalidate', {
          queryKeys: [
            tableKeys.list(projectRef, payload.schema, true),
            tableKeys.list(projectRef, payload.schema, false),
          ],
        });

        if (onSuccess) onSuccess(data, variables);
        return data;
      } catch (error) {
        if (onError) {
          onError(error, variables);
        } else {
          const message = error?.message || 'Failed to create database table';
          console.error(message);
        }
        throw error;
      }
    },

    mutate: function(variables, callbacks) {
      this.mutateAsync(variables)
        .then(data => callbacks?.onSuccess?.(data))
        .catch(error => callbacks?.onError?.(error));
    },
  };
}

export {
  createTable,
  useTableCreateMutation,
};
